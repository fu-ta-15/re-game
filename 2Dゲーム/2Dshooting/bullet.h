#ifndef _BULLET_H_
#define _BULLET_H_

#include "main.h"

//=====================================================================================================================================================================//
// マクロ定義
//=====================================================================================================================================================================//
#define MAX_BULLET		(256)		// 弾の最大数

//=====================================================================================================================================================================//
// プレイヤーの弾の種類
//=====================================================================================================================================================================//
typedef enum
{
	BULLET_TYPE_PLAYER_GREEN = 0,	// プレイヤー1の弾
	BULLET_TYPE_PLAYER_RED,			// プレイヤー2の弾
	BULLET_TYPE_ENEMY,				// 敵の弾
	BUULET_TYPE_GREEN_SKILL,		// 緑のスキル
	BUULET_TYPE_RED_SKILL,			// 赤のスキル
	BULLET_TYPE_MAX
}BULLET_TYPE;

//=====================================================================================================================================================================//
// 弾の構造体
//=====================================================================================================================================================================//
typedef struct
{
	D3DXVECTOR3 pos;				// 位置
	D3DXVECTOR3 move;				// 移動量
	BULLET_TYPE state;				// 現在の弾のタイプ
	int nLife;						// 寿命
	bool bSkill;					// スキルを使っているかどうか
	bool bUse;						// 使用しているかどうか
}BULLET;

//=====================================================================================================================================================================//
//プロトタイプ宣言
//=====================================================================================================================================================================//
HRESULT InitBullet(void);
void UninitBullet(void);
void UpdateBullet(void);
void DrawBullet(void);
void SetBullet(D3DXVECTOR3 pos, D3DXVECTOR3 move, int nLife, BULLET_TYPE Btype);

#endif 

