//=====================================================================================================================================================================//
// 列挙型
//=====================================================================================================================================================================//
typedef enum
{
	ACTION_MODE_ATTACK = 0,
	ACTION_MODE_DIG,
	ACTION_MODE_BAG,
	ACTION_MODE_NOWN,
	ACTION_MODE_MAX
}ACTION_MODE;

//=====================================================================================================================================================================//
//ポイント構造体
//=====================================================================================================================================================================//
typedef struct
{
	D3DXVECTOR3 pos;	// 位置
	D3DXVECTOR3 move;	// 移動
	D3DXVECTOR3 MOVE;	// 移動
	ACTION_MODE Amode;	// actionモード
	bool Action;
	bool Hit;
}ACTIONPLAYER;


