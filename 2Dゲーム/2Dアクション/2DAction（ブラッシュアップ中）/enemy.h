#ifndef _ENEMY_H_
#define _ENEMY_H_

#include "main.h"

//=====================================================================================================================================================================//
// マクロ変数
//=====================================================================================================================================================================//

//=====================================================================================================================================================================//
// 敵の種類
//=====================================================================================================================================================================//
typedef enum
{
	ENEMY_TYPE_MANY = 0,	// ざこ
	ENEMY_TYPE_JUELE,	// 赤専用
	ENEMY_TYPE_MAX
}ENEMY_TYPE;

//=====================================================================================================================================================================//
// 敵の状態
//=====================================================================================================================================================================//
typedef enum
{
	ENEMYSTATE_NORMAL = 0,	// 通常
	ENEMYSTATE_DAMAGE,		// ダメージ
	ENEMYSTATE_FALSE,		// 死亡
	ENEMYSTATE_STAY,		// 出現まち
	ENEMYSTATE_GO,			// 出現
	ENEMYSTATE_MAX
}ENEMYSTATE;

//=====================================================================================================================================================================//
// 敵の構造体
//=====================================================================================================================================================================//
typedef struct
{
	D3DXVECTOR3 pos;		// 位置
	D3DXVECTOR3 move;		// 位置
	D3DXVECTOR3 PlayerMove;
	ENEMY_TYPE Type;		// 種類
	ENEMYSTATE state;		// 状態
	int nCounterState;		// 状態のカウンター
	int nLife;				// 体力
	bool bScreenVisible;	// 画面内にいるかどうか
	bool bUse;				// 生存確認
}ENEMY;

//=====================================================================================================================================================================//
// プロトタイプ宣言
//=====================================================================================================================================================================//
HRESULT InitEnemy(void);
void UninitEnemy(void);
void UpdateEnemy(void);
void DrawEnemy(void);
void SetEnemy(D3DXVECTOR3 pos, D3DXVECTOR3 move, ENEMY_TYPE Type, int nLife);
void RandSetEnemy(void);
void StateEnemy(int nIdx);
void MoveEnemy(void);
bool HitEnemy(int nIdx, int nDamage);
ENEMY *GetEnemy(void);

#endif 

