#ifndef _CAMERA_H_
#define _CAMERA_H_

#include "main.h"

//=====================================================================================================================================================================//
// カメラの構造体
//=====================================================================================================================================================================//
typedef struct
{
	D3DXVECTOR3 posV;			// 視点(現在)
	D3DXVECTOR3 posR;			// 注視点(現在)
	D3DXVECTOR3 posVDest;		// 視点(目的)
	D3DXVECTOR3 posRDest;		// 注視点(目的)
	D3DXVECTOR3 moveX;			// 移動量
	D3DXVECTOR3 moveZ;			// 移動量
	D3DXVECTOR3 moveVY;			// 移動量
	D3DXVECTOR3 moveRY;			// 移動量
	D3DXVECTOR3 rot;			// カメラの向き
	D3DXVECTOR3 vecU;			// 上方向ベクトル
	D3DXMATRIX mtxProjection;	// プロジェクションマトリックス
	D3DXMATRIX mtxView;			// ビューマトリックス
}Camera;/* カメラ */

//=====================================================================================================================================================================//
// プロトタイプ宣言
//=====================================================================================================================================================================//
void InitCamera(void);
void UninitCamera(void);
void UpdateCamera(void);
void MoveCamera(void);
void SetCamera(void);
Camera *GetCamera(void);

#endif // !_CAMERA_H_
