#ifndef _POINT_H_
#define _POINT_H_

#include "main.h"

//=====================================================================================================================================================================//
// ポインター種類
//=====================================================================================================================================================================//
typedef enum
{
	POINTER_TYPE_ATTACK = 0,
	POINTER_TYPE_DIG,
	POINTER_TYPE_MAX
}POINTER_TYPE;

//=====================================================================================================================================================================//
//　ポインター構造体
//=====================================================================================================================================================================//
typedef struct
{
	D3DXVECTOR3 pos;	// 位置
	D3DXVECTOR3 move;	// 移動
	POINTER_TYPE type;
	bool bUse;
}POINTER;


//=====================================================================================================================================================================//
//プロトタイプ宣言
//=====================================================================================================================================================================//
HRESULT InitPoint(void);
void UninitPoint(void);
void UpdatePoint(void);
void DrawPoint(void);
POINTER *GetPointer(void);

#endif 

