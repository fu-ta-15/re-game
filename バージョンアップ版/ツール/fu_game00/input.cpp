#include "input.h"

//-----------------------------------------------------------------------------
// 静的メンバ変数
//-----------------------------------------------------------------------------
LPDIRECTINPUT8 CInput::m_pDInput;

//=============================================================================
// コンストラクタ
//=============================================================================
CInput::CInput()
{
}

//=============================================================================
// デストラクタ
//=============================================================================
CInput::~CInput()
{
}

//=============================================================================
// 初期化
//=============================================================================
HRESULT CInput::Init(HINSTANCE hInstance, HWND hWnd)
{
	// DirectInputオブジェクトの生成
	if (FAILED(DirectInput8Create(hInstance, DIRECTINPUT_VERSION, IID_IDirectInput8, (void**)&m_pDInput, NULL)))
	{
		return E_FAIL;
	}

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void CInput::Uninit(void)
{
	if (m_pDDevice != NULL)
	{// データフォーマットの開放
	 // アクセス権の破棄
		m_pDDevice->Unacquire();
		m_pDDevice->Release();
		m_pDDevice = NULL;
	}
	if (m_pDInput != NULL)
	{// DirectInputオブジェクトの開放
		m_pDInput->Release();
		m_pDInput = NULL;
	}
}
