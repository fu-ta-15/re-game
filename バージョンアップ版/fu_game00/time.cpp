//=============================================================================
// 時間の変更
//=============================================================================
void CTime::AddTime(int nValue)
{
	m_nTime += nValue;	// 時間の変更
	SetTime(m_nTime);	// 時間設定
}

//=============================================================================
// 時間の変更するためのカウント更新
//=============================================================================
int CTime::AddCnt(int nValue)
{
	return (m_nCntTime += 1);	// カウント情報を返す
}

//=============================================================================
// 時間の取得
//=============================================================================
int CTime::GetTime()
{
	return m_nTime;
}

//=============================================================================
// フレーム時間の取得
//=============================================================================
