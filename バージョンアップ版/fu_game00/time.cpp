//=============================================================================
// 時間の生成
//=============================================================================
CTime * CTime::Create(const D3DXVECTOR3 pos, const D3DXVECTOR3 size, const int nTime)
{
	CTime *pTime = NULL;

	if (pTime == NULL)
	{// NULLチェック
		pTime = new CTime;
		pTime->m_nTime = nTime;	 // 時間
		pTime->m_nCntTime = 0;	 // カウント初期化
		pTime->Init();
	}

	return pTime;
}

//=============================================================================
// 時間のポリゴンなどの生成
//=============================================================================
HRESULT CTime::Init()
{
	SetTime(TIME);	// 時間の設定

	return S_OK;
}

