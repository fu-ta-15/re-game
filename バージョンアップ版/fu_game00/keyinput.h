	CKey();
	~CKey();
	//-----------------------------------------------------------------------------
	// 入力の列挙型
	//-----------------------------------------------------------------------------
	typedef enum
	{
		STATE_NONE = 0,
		STATE_PRESSE,
		STATE_TRIGGER,
		STATE_RELEASE,
		STATE_MAX
	}STATE;
	//-----------------------------------------------------------------------------
	// メンバ関数
	//-----------------------------------------------------------------------------
	HRESULT Init(HINSTANCE hInstance, HWND hWnd);
	void Uninit(void);
	void Update(void);

	// Get関数
	bool GetState(STATE state,int nKey);

private:
	BYTE m_aKeyState[STATE_MAX][KEY_MAX];
